    internal static double[] UpsampleLinear(double[] orig, int upsampleFactor)
    {
        if (orig.Length < 2)
        {
            return orig.ToArray();
        }
        double[] upsampled = new double[(orig.Length - 1) * upsampleFactor];
        int iUpsampled = 0;
        double upsampleFactorD = upsampleFactor;
        for (int iOrig = 0; iOrig < orig.Length - 1; iOrig++)
        {
            double left = orig[iOrig];
            double right = orig[iOrig + 1];
            upsampled[iUpsampled] = left;
            iUpsampled++;
            int i = 1;
            while (i < upsampleFactor)
            {
                double cur = right * i / upsampleFactorD + left * (upsampleFactor - i) / upsampleFactorD;
                upsampled[iUpsampled] = cur;
                i++;
                iUpsampled++;
            }
        }
        upsampled[^1] = orig[^1];
        return upsampled;
    }

