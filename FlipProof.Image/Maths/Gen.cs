    public static double Median(Dictionary<double, int> counts)
    {
        int totalCount = counts.Sum((a) => a.Value);
        switch (totalCount)
        {
            case 0:
                throw new ArgumentException("No items");
            case 1:
                return counts.Keys.First();
            default:
                {
                    double[] keysInOrder = counts.Keys.ToArray();
                    int[] countsByKey = counts.Values.ToArray();
                    Array.Sort(keysInOrder, countsByKey);
                    int middle_Count = (int)Math.Ceiling(totalCount / 2.0);
                    int cumulative = 0;
                    int middleIsAtIndex = 0;
                    bool nextIsInSameBin = false;
                    for (; middleIsAtIndex < countsByKey.Length; middleIsAtIndex++)
                    {
                        int thisGoesUpToCount = cumulative + countsByKey[middleIsAtIndex];
                        if (thisGoesUpToCount >= middle_Count)
                        {
                            nextIsInSameBin = thisGoesUpToCount > middle_Count;
                            break;
                        }
                        cumulative = thisGoesUpToCount;
                    }
                    if (totalCount % 2 == 0)
                    {
                        int middleIsAtIndex_Right = !nextIsInSameBin ? middleIsAtIndex + 1 : middleIsAtIndex;
                        return (keysInOrder[middleIsAtIndex_Right] + keysInOrder[middleIsAtIndex]) / 2.0;
                    }
                    return keysInOrder[middleIsAtIndex];
                }
        }
    }

