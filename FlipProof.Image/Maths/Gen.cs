    public static int[] Primes(int count)
    {
        int[] primes = new int[count];
        int last = 1;
        primes[0] = 2;
        for (int i = 1; i < primes.Length; i++)
        {
            bool prime = true;
            int cur;
            for (cur = last + 2; cur < int.MaxValue; cur += 2)
            {
                prime = true;
                int divisor = Convert.ToInt32(Math.Sqrt(cur));
                if (divisor % 2 == 0)
                {
                    divisor++;
                }
                int indexOfLargestPrimeToCheck = primes.IndexOfLast((int a, int index) => a <= Convert.ToInt32(Math.Sqrt(cur)), i);
                for (int i_checkAgaint = 0; i_checkAgaint <= indexOfLargestPrimeToCheck; i_checkAgaint++)
                {
                    if (cur % primes[i_checkAgaint] == 0)
                    {
                        prime = false;
                        break;
                    }
                }
                if (prime)
                {
                    primes[i] = cur;
                    last = cur;
                    break;
                }
            }
            if (!prime)
            {
                throw new ArgumentException("Count is too high - we have reached maximum int32 precision");
            }
        }
        return primes;
    }

