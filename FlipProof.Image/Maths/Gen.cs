    public static int[] BinCounts(ICollection<float> vals, int noBins, double minBinCeiling, out double[] binMaxes)
    {
        double max = vals.Max();
        double binIncrement = (max - minBinCeiling) / noBins;
        binMaxes = (from a in GenMethods.IncrementalIEnum_F(1, noBins)
                    select minBinCeiling + binIncrement * (double)a).ToArray();
        binMaxes[noBins - 1] += Number.EpsilonOf(max);
        int[] binCounts = new int[noBins];
        foreach (float cur in vals)
        {
            int bin = binMaxes.IndexOf((double a) => a > (double)cur);
            binCounts[bin]++;
        }
        return binCounts;
    }

    public static double[] GetBinMiddleVals(double[] vals, int noBins)
