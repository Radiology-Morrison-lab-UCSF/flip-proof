    public static double Percentile(IList<double> vals, double percentile)
    {
        if (percentile <= 0.0 || percentile > 1.0)
        {
            throw new ArgumentOutOfRangeException("percentile");
        }
        if (vals.Count == 0)
        {
            throw new ArgumentException("vals has no items");
        }
        if (vals.Count == 1)
        {
            return vals[0];
        }
        vals = GenMethods.SortIListInPlace(vals.ToArray());
        double index = vals.Count * percentile - 1.0;
        double rem = index % 1.0;
        if (rem == 0.0)
        {
            return vals[(int)index];
        }
        int indexLeft = (int)Math.Ceiling(index);
        double valLeft = vals[indexLeft];
        double valRight = vals[indexLeft + 1];
        return (1.0 - rem) * valLeft + rem * valRight;
    }

    public static double Pythagoras(double side1, double side2)
    {
        double absX = Math.Abs(Convert.ToDouble(side1));
        double absY = Math.Abs(Convert.ToDouble(side2));
        double max;
        double min;
        if (absX > absY)
        {
            max = absX;
            min = absY;
        }
        else
        {
            max = absY;
            min = absX;
        }
        if (max == 0.0)
        {
            return 0.0;
        }
        double r = min / max;
        return max * Math.Sqrt(1.0 + r * r);
    }

    public static double Distance(double x0, double y0, double x1, double y1)
