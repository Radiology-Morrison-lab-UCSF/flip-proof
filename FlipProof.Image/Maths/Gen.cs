    public static double Covariance(double[] samples1, double[] samples2)
    {
        if (samples1 == null)
        {
            throw new ArgumentNullException("samples1");
        }
        if (samples2 == null)
        {
            throw new ArgumentNullException("samples2");
        }
        if (samples1.Length != samples2.Length)
        {
            throw new ArgumentException("Arguments are not the same length");
        }
        if (samples1.Length <= 1)
        {
            return double.NaN;
        }
        double mean1 = samples1.Average();
        double mean2 = samples2.Average();
        double covariance = 0.0;
        for (int i = 0; i < samples1.Length; i++)
        {
            covariance += (samples1[i] - mean1) * (samples2[i] - mean2);
        }
        return covariance / (samples1.Length - 1);
    }

