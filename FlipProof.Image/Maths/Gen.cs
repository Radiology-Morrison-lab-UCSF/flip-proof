        float meanBlockSize = num + 1f;
        float nodesPerBinSide = num / 2f;
        int[] binIndices = new int[noBins];
        for (int j = 0; j < binIndices.Length; j++)
        {
            binIndices[j] = Convert.ToInt32(nodesPerBinSide + j * meanBlockSize);
        }
        return binIndices.Select((i) => copy[i]).ToArray();
    }

